package com.julio.evento.model;

public class Alfaias {
	private String alfaia;

	public Alfaias(String alfaia) {
		super();
		this.alfaia = alfaia;
	}
	public Alfaias() {
		super();
	}

	/**
	 * @return the alfaia
	 */
	public String getAlfaia() {
		return alfaia;
	}

	/**
	 * @param alfaia
	 *            the alfaia to set
	 */
	public void setAlfaia(String alfaia) {
		this.alfaia = alfaia;
	}
}
