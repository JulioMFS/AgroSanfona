package com.julio.evento.model;

public class Eventos {
	private String eventos;

	public Eventos(String eventos) {
		super();
		this.eventos = eventos;
	}

	public Eventos() {
		super();
	}

	/**
	 * @return the eventos
	 */
	public String getEventos() {
		return eventos;
	}

	/**
	 * @param eventos
	 *            the eventos to set
	 */
	public void setEventos(String eventos) {
		this.eventos = eventos;
	}

}
